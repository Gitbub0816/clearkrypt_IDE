        ["string"] = B("#ce9178"),
        ["number"] = B("#b5cea8"),
        ["comment"] = B("#6a9955"),
        ["operator"] = B("#d4d4d4"),
        ["type"] = B("#4ec9b0"),
        ["model"] = B("#4ec9b0"),
        ["enum"] = B("#4ec9b0"),
        ["enumMember"] = B("#9cdcfe"),
        ["errorType"] = B("#f48771"),
        ["function"] = B("#dcdcaa"),
        ["parameter"] = B("#9cdcfe"),
