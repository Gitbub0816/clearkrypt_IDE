/// <summary>Draws dotted underlines beneath diagnostic ranges.</summary>
internal sealed class DiagnosticUnderlineRenderer : IBackgroundRenderer
{
    private static readonly Pen ErrorPen = new(new SolidColorBrush(Color.Parse("#f14c4c")), 1.5, DashStyle.Dot);
    private static readonly Pen WarningPen = new(new SolidColorBrush(Color.Parse("#cca700")), 1.5, DashStyle.Dot);

    private readonly TextEditor _editor;
    private IReadOnlyList<DiagnosticItemViewModel> _diagnostics = Array.Empty<DiagnosticItemViewModel>();

    public DiagnosticUnderlineRenderer(TextEditor editor)
    {
        _editor = editor;
    }

    public KnownLayer Layer => KnownLayer.Selection;

    public void SetDiagnostics(IReadOnlyList<DiagnosticItemViewModel> diagnostics) =>
        _diagnostics = diagnostics;

    public void Draw(TextView textView, DrawingContext drawingContext)
    {
        if (_diagnostics.Count == 0 || !textView.VisualLinesValid)
        {
            return;
        }
        var document = _editor.Document;
        foreach (var diagnostic in _diagnostics)
        {
            if (diagnostic.Line + 1 > document.LineCount)
            {
                continue;
            }
            var line = document.GetLineByNumber(diagnostic.Line + 1);
            var start = Math.Min(line.Offset + diagnostic.Character, line.EndOffset);
            var end = Math.Min(Math.Max(start + 1, line.EndOffset), line.EndOffset);
            if (end <= start)
            {
                end = Math.Min(start + 1, document.TextLength);
            }
            var segment = new TextSegment { StartOffset = start, EndOffset = end };
            var pen = diagnostic.Severity == DiagnosticSeverityLevel.Error ? ErrorPen : WarningPen;
            foreach (var rect in BackgroundGeometryBuilder.GetRectsForSegment(textView, segment))
            {
                var y = rect.Bottom - 1;
                drawingContext.DrawLine(pen, new Point(rect.Left, y), new Point(rect.Right, y));
            }
        }
    }
}
